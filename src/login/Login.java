    private void forgot_pwMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_forgot_pwMouseClicked

        String un = un_txt.getText();
        
        if(un.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please type user name as u remember!");
        }else{
            try {
                String sql = "select * from users where user_name=?";
                PreparedStatement ps = (PreparedStatement)connection.prepareStatement(sql);
                ps.setString(1, un);
                ResultSet rs = (ResultSet)ps.executeQuery();
                if(rs.first()){
                    String answ1 = rs.getString("answer1");
                    String answ2 = rs.getString("answer2");
                    String answ3 = rs.getString("answer3");
                    String q1 = rs.getString("sec_ques1");
                    String q2 = rs.getString("sec_ques2");
                    String q3 = rs.getString("sec_ques3");
                    Register reg = new Register();
                    reg.setSecurityQuestions(un,answ1, answ2, answ3, q1, q2, q3);
                    reg.setVisible(true);
                    reg.pack();
                    reg.setLocationRelativeTo(null);
                    this.dispose();
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Try Again!There is no username related to given!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_forgot_pwMouseClicked

